    var pacdots: List<List<PacDot?>>

    init {
        pacdots = laberinto.mapIndexed { y, fila ->
            fila.mapIndexed { x, celda ->
                if (celda == 1) PacDot(SkinLoader.skin.atlas, x, y) else null
            }
        }
    }

    fun render(batch: SpriteBatch, pacman: PacMan) {
        pacdots.forEach { fila ->
            fila.forEach { pacdot ->
                pacdot?.render(batch, pacman)
            }
        }
    }

    fun resetPacdots() {
        pacdots = laberinto.mapIndexed { y, fila ->
            fila.mapIndexed { x, celda ->
                if (celda == 1) PacDot(SkinLoader.skin.atlas, x, y) else null
            }
        }
    }

    fun calcularPuntuacion(): Int {
        var puntuacion = 0
        pacdots.forEach { fila ->
            fila.forEach { pacdot ->
                if (pacdot?.comido == true) {
                    puntuacion += 10
                }
            }
        }
        return puntuacion
    }


    fun x(x: Int) = x * RzGraphicController.scale() * RzGraphicController.tamFrame
    fun y(y: Int) =
        Gdx.graphics.height - (Gdx.graphics.height - ((laberinto.size - 2) * RzGraphicController.tamFrame * RzGraphicController.scale())) / 2 - (y * RzGraphicController.scale() * RzGraphicController.tamFrame)
